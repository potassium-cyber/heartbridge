def generate_nickname(role):
    """
    根据角色生成随机昵称。
    
    Args:
        role (str): '家长' 或 '孩子'
        
    Returns:
        str: 组合后的昵称，例如 "试图弄懂流行语的考拉"
    """
    if role not in NICKNAME_DATA:
        return "神秘的路人甲"
    
    adj = random.choice(NICKNAME_DATA[role]["adjectives"])
    noun = random.choice(NICKNAME_DATA[role]["nouns"])
    
    return f"{adj}{noun}"

